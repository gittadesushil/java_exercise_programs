package Looping;

public class Fun {
    public static void main(String[] args) {
        jump();
        run();
        jump();
    }

    private static void jump() {
        System.out.println("jumping");
        System.out.println("jumping");
        System.out.println("jumping");
        System.out.println("jumping");
        System.out.println("jumping");
        System.out.println("jumping");
    }

    private static void run() {
        System.out.println("running");
        System.out.println("running");
    }
}
